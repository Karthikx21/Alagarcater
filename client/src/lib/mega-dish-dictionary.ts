// Function to search dishes by name
export function searchDishes(searchTerm: string): Array<{en: string, ta: string}> {
  const results: Array<{en: string, ta: string}> = [];
  const lowerSearchTerm = searchTerm.toLowerCase();
  
  for (const [key, value] of Object.entries(comprehensiveDishTranslations)) {
    if (key.toLowerCase().includes(lowerSearchTerm) || 
        value.en.toLowerCase().includes(lowerSearchTerm) ||
        value.ta.includes(searchTerm)) {
      results.push(value);
    }
  }
  
  return results;
}

